def main():
    # 内容分批放在 lc_hot100_part*.py，每个文件导出 PROBLEMS 列表
    problems = []
    for i in range(1, 8):
        mod = __import__(f"lc_hot100_part{i}")
        problems.extend(mod.PROBLEMS)
    assert len(problems) == 100, f"题目数={len(problems)}，应为 100"

    conn = sqlite3.connect(DB, timeout=30)
    conn.execute("PRAGMA foreign_keys=ON")
    for ord_, (slug, name, intro) in enumerate(CATEGORIES, 1):
        conn.execute(
            "INSERT INTO lc_categories(slug, name, ord, intro) VALUES (?,?,?,?) "
            "ON CONFLICT(slug) DO UPDATE SET name=excluded.name, ord=excluded.ord, intro=excluded.intro",
            (slug, name, ord_, intro.strip()))

    for ord_, p in enumerate(problems, 1):
        url = f"https://leetcode.cn/problems/{p['slug']}/"
        cur = conn.execute("SELECT id FROM lc_problems WHERE lc_id=?", (p["lc"],)).fetchone()
        if cur:  # 重复执行：更新内容但保留 done 状态
            conn.execute(
                "UPDATE lc_problems SET category=?, ord=?, title=?, difficulty=?, url=?, content=? WHERE id=?",
                (p["cat"], ord_, p["t"], p["d"], url, p["md"].strip(), cur[0]))
        else:
            conn.execute(
                "INSERT INTO lc_problems(category, ord, lc_id, title, difficulty, url, content) "
                "VALUES (?,?,?,?,?,?,?)",
                (p["cat"], ord_, p["lc"], p["t"], p["d"], url, p["md"].strip()))
    conn.commit()
    n = conn.execute("SELECT COUNT(*) FROM lc_problems").fetchone()[0]
    print(f"✓ 已写入 {len(CATEGORIES)} 个分类、{n} 道题")
    conn.close()


if __name__ == "__main__":
    main()
